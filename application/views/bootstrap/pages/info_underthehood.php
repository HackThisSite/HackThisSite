<a name="hardwareandhosting"></a>
<h2>Hardware & Hosting</h2>
HackThisSite operates a variety of systems to host all of our services.  Included in this list are both owned physical machinery and miscellaneous virtualized or shared resources.

<a name="physicalhardware"></a>
<h3>Physical Hardware</h3>
HackThisSite currently owns and operates two dedicated servers in Chicago, Illinois, USA.  Their specifications are as follows:
<ul>
<li>AMD Athlon(tm) 64 X2 Dual Core Processor 4000+, 4GB RAM, dual 750GB drives in RAID-1, 100Mbps uplink</li>
<li>Intel(R) Xeon(TM) CPU 2.80GHz, 12GB RAM, dual 1TB drives in RAID-1, 100Mbps uplink</li>
</ul>

<a name="otherhosting"></a>
<h3>Other Hosting</h3>
Due to the fact that we require high-availability across multiple geographical locations, we also utilize a series of Virtual Private Servers (VPSes) and shell servers around the globe.  They are as follows:
<ul>
<li>Two VPSes in Secaucus, New Jersey, USA</li>
<li>One Shell in Appleton, Wisconsin, USA</li>
<li>One Shell in Stockholm, Sweden, EU</li>
</ul>

<a name="software"></a>
<h2>Software</h2>
To provide all the services we do, HackThisSite utilizes a very large assortment of software, some made in-house, but mostly heavily customized and configured free and open source software.

<a name="softwarewedevelop"></a>
<h3>Software We Develop</h3>
The code that runs the entire main HackThisSite website, from the main landing page to the missions themself, are all developed by a team of skilled developers.  The only exception to this are the forums, which use phpBB.

